// Intersection Observer for animations
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('fade-in-up');
        }
    });
}, observerOptions);

// Observe all sections for animation
document.querySelectorAll('section').forEach(section => {
    observer.observe(section);
});

// Observe project cards for staggered animation
document.querySelectorAll('.project-card').forEach((card, index) => {
    card.style.animationDelay = `${index * 0.1}s`;
    observer.observe(card);
});

// Observe skill items for staggered animation
document.querySelectorAll('.skill-item').forEach((item, index) => {
    item.style.animationDelay = `${index * 0.05}s`;
    observer.observe(item);
});

// Typing effect for hero title
function typeWriter(element, text, speed = 100) {
    // Store the original HTML content
    const originalHTML = element.innerHTML;
    
    // Extract text content without HTML tags
    const tempDiv = document.createElement('div');
    tempDiv.innerHTML = originalHTML;
    const textContent = tempDiv.textContent || tempDiv.innerText || '';
    
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < textContent.length) {
            // Reconstruct the HTML with the current text
            const currentText = textContent.substring(0, i + 1);
            element.innerHTML = originalHTML.replace(textContent, currentText);
            i++;
            setTimeout(type, speed);
        } else {
            // Ensure final HTML is correct
            element.innerHTML = originalHTML;
        }
    }
    type();
}

